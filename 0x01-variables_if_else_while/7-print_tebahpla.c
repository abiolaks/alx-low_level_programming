#include <stdio.h>

/**
 * main - Entry point
 *
 * Return: Always 0 (success)
 *
 * putchar - print character
 *
 * Description: The below code prints the lower case
 * alphabet in reverer using only the putchar function.
 */
int main(void)
{
	char i;

	for (i = 0172; i >= 0141; --i)
	{
		putchar(i);
	}
	putchar('\n');
	return (0);
}
